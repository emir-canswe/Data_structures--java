package classs;

// Bu sınıf bir düğümü (node) temsil eder.
public class classass2 {
    int data; // Düğümde tutulacak veri
    classass2 next; // Sonraki düğüm bağlantısı
    classass2 prev; // Önceki düğüm bağlantısı

    // Constructor: veri parametresiyle düğüm oluşturulur
    public classass2(int d) {
        data = d;
        next = null;
        prev = null;
    }
}
